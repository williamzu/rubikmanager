    private void confirmaSaida() {                                     
            int result = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja sair?", "Sair", JOptionPane.YES_NO_OPTION);  
            if (result == JOptionPane.YES_OPTION) {  
                  System.exit(0);
            }
            
            
    }   
    
    

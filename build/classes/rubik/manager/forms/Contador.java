    private void contadorLogica(JButton botao, JLabel cronometro) throws SQLException{
        
        nf = NumberFormat.getNumberInstance(new Locale("en", "US"));
        timeFormat = (DecimalFormat)nf;
	timeFormat.applyPattern("00.00");
        if (botao.getText().equals("Disparar Cron√¥metro!")) {
            botao.setText("Parar");
            t1 = System.currentTimeMillis();
        } else if(botao.getText().equals("Parar")){
                    botao.setText("Pr√≥ximo Tempo!");
                    t2 = System.currentTimeMillis() - t1;            
                    cronometro.setText(timeFormat.format(t2/1000) +"s");
                    switch(opcao){
                    case 1: jLTempo1.setText(timeFormat.format(t2/1000) +"");
                            opcao = 2;
                            media= t2;
                            break;
                    case 2: jLTempo2.setText(timeFormat.format(t2/1000) +"");
                            opcao = 3;
                            media= t2+media;
                            break;
                    case 3: jLTempo3.setText(timeFormat.format(t2/1000) +"");
                            opcao = 4;
                            media= t2+media;
                            break;
                    case 4: jLTempo4.setText(timeFormat.format(t2/1000) +"");
                            opcao = 5;
                            media= t2+media;
                            break;
                    case 5: jLTempo5.setText(timeFormat.format(t2/1000) +"");
                            opcao = 1;
                            media= (t2+media)/1000;
                            botao.setText("Salvar sess√£o");
                            jLMedia.setText(timeFormat.format(media/5) +"");
                            jLMelhorTempo.setText(timeFormat.format(menorValor(jLTempo1, jLTempo2, jLTempo3, jLTempo4, jLTempo5))+"");
                            jLPiorTempo.setText(timeFormat.format(maiorValor(jLTempo1, jLTempo2, jLTempo3, jLTempo4, jLTempo5))+"");
                            break;
                    }
                } else if (botao.getText().equals("Pr√≥ximo Tempo!")){
                            cronometro.setText("00.00s");
                            botao.setText("Disparar Cron√¥metro!");
                       }else if(botao.getText().equals("Salvar sess√£o")){
                               salvarSessao(jLTempo1, jLTempo2, jLTempo3, jLTempo4, jLTempo5, jLMelhorTempo);
                         
                               

                             }
        
    }
    
